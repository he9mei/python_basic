#4.逻辑运算符 and or not
#返回类型是：布尔值 true false
# and 两边都为真才为真 ，返回true ;否则返回 false。 -->真真为真
# or 一边为真即为真，返回true ; 两边都为假则为假，返回 false -->假假为假
# not 取反
age=16
sex="female"
print(age>=18 and sex=="female")  #false and true
print(age>=18 or sex=="female")  #false or true
print(not(age>=18))  #false 取反




